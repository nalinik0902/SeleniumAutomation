package Day48;

public class AboutFramework {
   /*
	Framework- helps to organize the project related files and folders in a structured manner.
	Objective of framework:
		1. Reusability
		2. Maintainability
		3. Readability
	
	
	Types of framework:
		1. Built-in framework - Testng, Junit, Cucumber, robot framework
		2. Customized framework - data driven,keywork driven,hybrid driven and modular framework
		
   Automation Process
   1. Analysing AUT (once we get the builds from developer)
       - Number of pages
       - what are all the elements , type of elements
       - what can automate and what cannot automate
   2. Choose test cases for Automation
      Interview question:
      100% automation of entire project is not possible
      100% automation is possible for the testcases which can be automated. 
       Eg: Among 100 testcases, 90 testcases can automate and 10 testcases cannot automate.
           100% automation can be done on 90 testcases
           
          Testcase priority:
          1. Sanity testcases(Basic functionality testcase) -P1
          2. Data driven testcases(testcases that requires testdata )/ retests - P2
          3. Regression testcase(once after the bugfixes) - P3
          4. Other testcase - P4
   3. Design and Development of Framework
         Interview question: Do you implement framework ?
         Answer- based on experience level.
         I contributed to framework . what contribution ? - Creating pageobjects, test cases
   4. Execution - Testcase execution locally or remotely 
       local means - running testcase in our system
       remote means - running testcase in different environments/servers
   5. Maintenance- keeping the project in central repository so that everyone in the team can collobrate /contribute.                    
      
*/
}
