package JavaOOPSConcept;

public class MethodsVSConstructors {
	/*
	Methods:
		1. Method name can be anything
		2. Method may or maynot return values
	    3. If method is not returning any vaue then specify void
		3. Method can have parameter or arguments
		4. we have to call methods explicitly throug objects. 
		5. used for specifying logic.
	 */
	
	/*
	Constructors:
		1. Constructor name should be name as class name.
		2. constructor will never return a value
		3. no need to mention void
		4.constructor can have paarmeter/arguments
		5.constructor automatically created at the time of object creation
		6. used for initializing the value of the variables
	*/			

}
